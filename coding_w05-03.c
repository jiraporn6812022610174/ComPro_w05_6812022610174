#include <stdio.h> // เรียกใช้ไลบรารีมาตรฐานสำหรับ printf

int main() {
// ประกาศและกำหนดค่าตัวแปรแบบทศนิยม
float a = 0.617;
int b = 25;
float c = 19.000;
int d = 9;
float e = 124.555;
float f = 0.001;

// แสดงผลโดยจัดรูปแบบให้อ่านง่าย โดยใช้ printf และจัดตำแหน่งด้วย field width

// บรรทัดที่ 1: แสดงค่าของ a โดยให้แสดงทศนิยม 3 ตำแหน่ง
printf("a = %.3f %%\n", a);

// บรรทัดที่ 2: แสดงค่าของ b โดยให้ชิดขวาในช่องกว้าง 8 ตัวอักษร
printf("b = %d %%\n", b);

// บรรทัดที่ 3: แสดงค่าของ c โดยให้แสดงทศนิยม 3 ตำแหน่ง
printf("c = %-.3f %%\n", c);

// บรรทัดที่ 4: แสดงค่าของ d โดยให้ชิดขวาในช่องกว้าง 8 ตัวอักษร
printf("d = % d%%\n", d);

// บรรทัดที่ 5: แสดงค่าของ e โดยให้แสดงทศนิยม 3 ตำแหน่ง
printf("e = %8.3f %%\n", e);

// บรรทัดที่ 6: แสดงค่าของ f โดยให้แสดงทศนิยม 3 ตำแหน่ง
printf("f = %-.3f %%\n", f);

return 0; // คืนค่า 0 แสดงว่าโปรแกรมทำงานสำเร็จ
}